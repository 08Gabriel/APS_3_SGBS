    private void alteraDados() {
        UsuarioController usuarioController = new UsuarioController();
        // PEGANDO NOME E SOBRENOME
        usuario.setNome(txtNome.getText());
        usuario.setSobrenome(txtSobrenome.getText());

        // VERIFICANDO SE O NOME DE USUARIO JÁ EXISTE
        if (!txtNomeDeUsuario.getText().equals(usuario.getNomeDeUsuario())) {
            try {
                if(!usuarioController.nomeDeUsuarioJaExiste(txtNomeDeUsuario.getText())) {
                    usuario.setNomeDeUsuario(txtNomeDeUsuario.getText());

                } else {
                    JOptionPane.showMessageDialog(this, "Nome de Usuário Indisponível", "ERRO", JOptionPane.ERROR_MESSAGE);

                }
            } catch (ExceptionDAO ex) {
                JOptionPane.showMessageDialog(this, "ERRO AO CONSULTAR NOME DE USUARIO\nERRO: " + ex, "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }

        // VERIRIFICANDO A SENHA ATUAL DO USUARIO
        if (jPasswordFieldSenhaAtual.getText().equals(usuario.getSenha())) {
            // VERIFICANDO SE A NOVA SENHA E CONFIRMAÇÃO DE SENHA SÃO IGUAIS
            if(jPasswordFieldNovaSenha.getText().equals("") && jPasswordFieldConfirmNovaSenha.getText().equals("")) 
            {
                try {
                    usuarioController.atulizarUsuario(usuario);
                    JOptionPane.showMessageDialog(this, "Dados Atulizados!", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);

                } catch (ExceptionDAO ex) {
                    JOptionPane.showMessageDialog(this, "ERRO AO ATUALIZAR USUARIO\nERRO: " + ex, "ERRO", JOptionPane.ERROR_MESSAGE);

                }
            } else if (jPasswordFieldNovaSenha.getText().equals(jPasswordFieldConfirmNovaSenha.getText())) 
            {
                try {
                    usuario.setSenha(jPasswordFieldNovaSenha.getText());
                    usuarioController.atulizarUsuario(usuario);
                    JOptionPane.showMessageDialog(this, "Dados Atulizados!", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);

                } catch (ExceptionDAO ex) {
                    JOptionPane.showMessageDialog(this, "ERRO AO ATUALIZAR USUARIO\nERRO: " + ex, "ERRO", JOptionPane.ERROR_MESSAGE);

                }
            } else {
                JOptionPane.showMessageDialog(this, "As novas senhas não conferem!", "ERRO", JOptionPane.ERROR_MESSAGE);

            }

        }
        else {
            JOptionPane.showMessageDialog(this, "Senha Incorreta", "ERRO", JOptionPane.ERROR_MESSAGE);

        }
    }
    
    private boolean validaAdm() throws ExceptionDAO {
        UsuarioController usuarioController = new UsuarioController();
        boolean validado = false;
        JTextField usr = new JTextField(5);
        JPasswordField senha = new JPasswordField(5);

        JPanel panelValidaAdm = new JPanel();
        panelValidaAdm.add(new JLabel("Usuário:"));
        panelValidaAdm.add(usr);
        panelValidaAdm.add(Box.createHorizontalStrut(5)); // espaço
        panelValidaAdm.add(new JLabel("Senha:"));
        panelValidaAdm.add(senha);

        int result = JOptionPane.showConfirmDialog(null, panelValidaAdm, 
               "VALIDAÇÃO DE ADMINISTRADOR", JOptionPane.OK_CANCEL_OPTION);
      
        if (result == JOptionPane.OK_OPTION) {
            validado = usuarioController.validarUsuarioAdmin(usr.getText(), senha.getText());
        }
        return validado;
        
    }
    
    private void atualizaCampos() {
        // ALTERANDO CAMPOS COM INFORMAÇÕES DO USUÁRIO ATUAL
        txtNome.setText(usuario.getNome());
        txtSobrenome.setText(usuario.getSobrenome());
        txtNomeDeUsuario.setText(usuario.getNomeDeUsuario());
        lblFunc.setText(usuario.getNumeroFuncional());
        
        if (usuario.getAcessoLab().toUpperCase().equals("SIM"))
            jComboBoxAcessoLab.setSelectedItem("Sim");
        else 
            jComboBoxAcessoLab.setSelectedItem("Não");
        
        if (usuario.getAdmin().toUpperCase().equals("SIM"))
            jComboBoxAdmin.setSelectedItem("Sim");
        else 
            jComboBoxAdmin.setSelectedItem("Não");
    }

