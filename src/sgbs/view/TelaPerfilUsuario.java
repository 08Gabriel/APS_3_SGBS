    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:);
        if (txtSobrenome.getText().equals(usuario.getSobrenome()) && txtNome.getText().equals(usuario.getNome())
            && txtNomeDeUsuario.getText().equals(usuario.getNomeDeUsuario()) && jPasswordFieldSenhaAtual.getText().equals("")
                && jPasswordFieldNovaSenha.getText().equals("") && jPasswordFieldConfirmNovaSenha.getText().equals("") &&
                jComboBoxAdmin.getSelectedItem().toString().equals(usuario.getAdmin()) && jComboBoxAcessoLab.getSelectedItem().toString().equals(usuario.getAcessoLab())) 
        {
            JOptionPane.showMessageDialog(this, "Nada foi alterado!", "Saindo", JOptionPane.INFORMATION_MESSAGE);
        
        
        } else {
            // SE OS ACESSOS CONTINUAM IGUAIS NAO É NECESSAORIO VALIDACAO DO ADMINISTRADOR
            if (jComboBoxAdmin.getSelectedItem().toString().equals(usuario.getAdmin()) && jComboBoxAcessoLab.getSelectedItem().toString().equals(usuario.getAcessoLab())) 
            { 
                alteraDados();
                atualizaCampos();
            
            } else // OS DADOS DE ACESSO FORAM ALTERADOS, NECESSARIO PERMISAO DO ADMINISTRADOR
            { 
                String novoAcessoLab = jComboBoxAcessoLab.getSelectedItem().toString();
                String novoAdmin = jComboBoxAdmin.getSelectedItem().toString();
                
                try {
                    if (validaAdm()) {
                        usuario.setAcessoLab(novoAcessoLab);
                        usuario.setAdmin(novoAdmin);
                        alteraDados();
                        atualizaCampos();
                    } else {
                        JOptionPane.showMessageDialog(this, "Não possível atualizar suas permissões de acesso", "Administrador inválido", JOptionPane.ERROR_MESSAGE);
                        
                    }
                } catch (ExceptionDAO ex) {
                    JOptionPane.showMessageDialog(this,"Erro ao validar administrador\nErro: " + ex, "ERRO", JOptionPane.ERROR_MESSAGE);

                }
                
            }
        }
            
    }//GEN-LAST:event_btnSalvarActionPerformed

