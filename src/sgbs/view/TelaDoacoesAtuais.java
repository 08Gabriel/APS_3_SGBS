    public final void lerModeloTable() throws ExceptionDAO {
        DefaultTableModel modelo = (DefaultTableModel) jTableDoacoesAtuais.getModel();
        modelo.setNumRows(0);
        
        DoacaoController doacaoController = new DoacaoController();
        
        DoadorController doadorController = new DoadorController();
        
        // LENDO VALORES DA LISTA RETORNADA
        for (Doacao d:doacaoController.leituraDoacao("Aguardando Analise")) {
            modelo.addRow(new Object[] {
                d.getId_Doacao(),
                d.getId_Doador(),
                d.getIdUsuario(),
                doadorController.obterNomeCompletoDoador(d.getId_Doador()),
                d.getDataColeta(),
                d.getQtdColetada(),
                d.getStatus()
            });
        
        }
    }
