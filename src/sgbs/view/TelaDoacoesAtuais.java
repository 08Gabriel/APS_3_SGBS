    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        if(jComboBoxParametroPesquisa.getSelectedItem().equals("Selecione")){
            JOptionPane.showMessageDialog(this, "Selecione uma opção", "Opção inválida", JOptionPane.ERROR_MESSAGE);;
        } else {
            try {
                String paramBusca = jComboBoxParametroPesquisa.getSelectedItem().toString();
                Integer id = Integer.parseInt(txtBuscaDoacao.getText());
                lerModeloTableParaPesquisa(paramBusca, id);
            } catch (ExceptionDAO ex) {
                JOptionPane.showMessageDialog(this, "ERRO INTERNO AO LER DADOS\nERRO: "  +ex, "ERRO", JOptionPane.ERROR_MESSAGE);
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Valor de busca vazio !!!", "Erro", JOptionPane.ERROR_MESSAGE);;

            }
        }
                

            
        
        
        
        
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void jTableDoacoesAtuaisMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableDoacoesAtuaisMouseClicked
        // AQUI PEGA A LINHA SELECONADA COM O CLIQUE
        int linha = jTableDoacoesAtuais.getSelectedRow();
        DefaultTableModel modelo = (DefaultTableModel) jTableDoacoesAtuais.getModel();
               
        // SETANDO OS VALORES DOS CAMPOS LINHA: OBTIDA COM O CLIQUE, INDICE COLUNA É FIXO (LINHA, COLUNA)
        txtIdDoacao.setText(modelo.getValueAt(linha,0).toString());
        txtIdDoador.setText(modelo.getValueAt(linha,1).toString());
        txtIdUsuario.setText(modelo.getValueAt(linha,2).toString());
        txtNomeDoador.setText(modelo.getValueAt(linha,3).toString());
        txtDtColeta.setText(modelo.getValueAt(linha,4).toString());
        txtQtdColetada.setText(modelo.getValueAt(linha,5).toString());
    }//GEN-LAST:event_jTableDoacoesAtuaisMouseClicked

    private void btnSalvaAlteracaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvaAlteracaoActionPerformed
        // TODO add your handling code here:
        boolean sucesso;
        
        try {
            Integer idDoacao = Integer.parseInt(txtIdDoacao.getText());
            String resultado = jComboBoxResultadoAnalise.getSelectedItem().toString();
            String motivoResultado = jTextAreaMotivoResultado.getText();
        

            AnaliseController analiseController = new AnaliseController();
            sucesso = analiseController.cadastrarAnalise(idDoacao, resultado, motivoResultado, data.substring(0, 10));
            
            if (sucesso) {
                JOptionPane.showMessageDialog(this, "Resultado Salvo", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                resetaCampos();
                lerModeloTable();
            } else {
               JOptionPane.showMessageDialog(this, "Campos Não foram preenchidos corretamente !!!", "Tente novamente", JOptionPane.ERROR_MESSAGE);

            }
        } catch(ExceptionDAO e) {
            JOptionPane.showMessageDialog(this, "ERRO: " + e);
            
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Selecione uma doação na tabela !!!", "Tente novamente", JOptionPane.ERROR_MESSAGE);

        }
        
        
    }//GEN-LAST:event_btnSalvaAlteracaoActionPerformed

