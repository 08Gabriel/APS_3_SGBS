    private void btnValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidarActionPerformed
        idDoador = Integer.parseInt(jSpinnerIdDoador.getValue().toString());
        cpf = txtCpf.getText();
        try {
            if(escolhidoParaValidar.equals("cpf")) {
                cadastrado = doadorController.verificarDoador(cpf);
                TelaTriagem.setCpfDoador(cpf);
            } else if (escolhidoParaValidar.equals("id")) {
                cadastrado = doadorController.verificarDoador(idDoador);
                TelaTriagem.setIdDoador(idDoador);
            }
            // O DOADOR FOI ENCONTRADO NO BANCO DE DADOS
            if (cadastrado) { 
                JOptionPane.showMessageDialog(this, "Doador encontrado !!!", "Liberado para inserir nova doação", JOptionPane.INFORMATION_MESSAGE);
                jPanelValidaDoador.setVisible(false);
                
                // PROCESSO DE TRIAGEM

                TelaTriagem triagem = new TelaTriagem();
                triagem.setVisible(true);
                this.dispose();
                
                // OBTENDO DADOS DO DOADOR
                if (escolhidoParaValidar.equals("cpf")) {
                    doador = sgbs.dao.DoadorDAO.carregarDadosDoador(cpf);
                } else if (escolhidoParaValidar.equals("id")) {
                    doador = sgbs.dao.DoadorDAO.carregarDadosDoador(idDoador);
                }
                
                
            } else {
                int opcao;
                opcao = JOptionPane.showConfirmDialog(this, "Deseja Cadastrar um novo doador ?", "Doador Não encontrado", JOptionPane.YES_NO_OPTION);
                
                if (opcao == 0 ) {
                    this.dispose();
                    TelaCadastrarDoador telaCadastrarDoador = new TelaCadastrarDoador();
                    telaCadastrarDoador.setVisible(true);
                }
            }
        } catch (ExceptionDAO ex) {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro\nERRO: " + ex, "Tente novamente", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnValidarActionPerformed

