    private void jComboBoxOpcaoDeValidacaoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBoxOpcaoDeValidacaoItemStateChanged
        
        if (jComboBoxOpcaoDeValidacao.getSelectedItem().equals("CPF")) 
        {   
            jLabelTituloOpcaoValidacao.setVisible(true);
            jLabelTituloOpcaoValidacao.setText("Insira o CPF do doador");
            escolhidoParaValidar = "cpf";
            cpf = null;
            jSpinnerIdDoador.setVisible(false);
            txtCpf.setVisible(true);
            btnValidar.setEnabled(true);
    
        } else if (jComboBoxOpcaoDeValidacao.getSelectedItem().equals("ID Doador")) 
        {
            jLabelTituloOpcaoValidacao.setVisible(true);
            jLabelTituloOpcaoValidacao.setText("Insira o ID do doador");
            escolhidoParaValidar = "id";
            idDoador = null;
            txtCpf.setVisible(false);
            jSpinnerIdDoador.setVisible(true);
            btnValidar.setEnabled(true);
           
        } else
        {
            jLabelTituloOpcaoValidacao.setText("NENHUMA OPÇÃO SELECIONADA");
            txtCpf.setVisible(false);
            jSpinnerIdDoador.setVisible(false);
            btnValidar.setEnabled(false);
        }

    }//GEN-LAST:event_jComboBoxOpcaoDeValidacaoItemStateChanged

